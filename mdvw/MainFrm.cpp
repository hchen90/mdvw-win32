void CMainFrame::OnFileOpen()
{
	CString f;
	CFileDialog dlg(TRUE);

	OPENFILENAME& ofn = dlg.GetOFN();
	ofn.lpstrDefExt = _T("md");
	ofn.lpstrFile = f.GetBuffer(MAX_PATH);
	ofn.nMaxFile = MAX_PATH;
	ofn.lpstrFilter = _T("MarkDown Files(*.md)\0*.md\0All Files(*.*)\0*.*\0");

	if (ofn.lpstrFile && dlg.DoModal() == IDOK) {
		//m_wndOutput.AddString(0, f);
		if (trans::isMD(f)) {
			TCHAR* p = new TCHAR[MAX_PATH];
			if (p && trans::tempfile(p, trans::pathtitle(f))) {
				trans::trans_start(f, p);
				theApp.OpenDocumentFile(p);
			}
			if (p) delete[] p;
		}
		else {
			theApp.OpenDocumentFile(f);
		}
	}

	if (ofn.lpstrFile) f.ReleaseBuffer();

}


void CMainFrame::OnDropFiles(HDROP hDrop)
{
	TCHAR buf[MAX_PATH];
	if (DragQueryFile(hDrop, 0, buf, sizeof(buf))) {
		if (trans::isMD(buf)) {
			TCHAR* p = new TCHAR[MAX_PATH];
			if (p && trans::tempfile(p, trans::pathtitle(buf))) {
				trans::trans_start(buf, p);
				theApp.OpenDocumentFile(p);
			}
			if (p) delete[] p;
		}
		else {
			theApp.OpenDocumentFile(buf);
		}
	}
	DragFinish(hDrop);

	CFrameWndEx::OnDropFiles(hDrop);
}